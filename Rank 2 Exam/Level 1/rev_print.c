#include <unistd.h>

int   main(int ac, char **av)
{
   char  *str;
   int   i;

   if (ac == 2)
   {
      str = av[1];
      i = 0;
      while (str[i] != '\0')
         i++;
      while (i-- > 0)
         write (1, &str[i], 1);
   }
   write(1, "\n", 1);
   return (0);
}